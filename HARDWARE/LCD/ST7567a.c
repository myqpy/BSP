void dispchar(unsigned char page,unsigned char CaddrH,unsigned char CaddrL,unsigned char size, unsigned char num)
{
    unsigned char i;
    unsigned char num_local = num - ' ';
    if(size == 24)
    {
        SendCmdByte(page);
        SendCmdWord(CaddrH,CaddrL);
        for(i=0; i<12;)
        {
            SendDataByte(asc2_2412[num_local][i++]);
        }

        SendCmdByte(page+0x01);
        SendCmdWord(CaddrH,CaddrL);
        for(i=12; i<24;)
        {
            SendDataByte(asc2_2412[num_local][i++]);
        }

        SendCmdByte(page+0x02);
        SendCmdWord(CaddrH,CaddrL);
        for(i=24; i<36;)
        {
            SendDataByte(asc2_2412[num_local][i++]);
        }
    }
    else if(size == 16)
    {
        SendCmdByte(page);
        SendCmdWord(CaddrH,CaddrL);
        for(i=0; i<8;)
        {
            SendDataByte(asc2_1608[num_local][i++]);
        }

        SendCmdByte(page+0x01);
        SendCmdWord(CaddrH,CaddrL);
        for(i=8; i<16;)
        {
            SendDataByte(asc2_1608[num_local][i++]);
        }
    }
    else if(size == 12)
    {
        SendCmdByte(page);
        SendCmdWord(CaddrH,CaddrL);
        for(i=0; i<6;)
        {
            SendDataByte(asc2_1206[num_local][i++]);
        }

        SendCmdByte(page+0x01);
        SendCmdWord(CaddrH,CaddrL);
        for(i=6; i<12;)
        {
            SendDataByte(asc2_1206[num_local][i++]);
        }
    }
    else if(size == 32)
    {
        SendCmdByte(page);
        SendCmdWord(CaddrH,CaddrL);
        for(i=0; i<16;)
        {
            SendDataByte(Hzk1[num][i++]);
        }

        SendCmdByte(page+0x01);
        SendCmdWord(CaddrH,CaddrL);
        for(i=16; i<32;)
        {
            SendDataByte(Hzk1[num][i++]);
        }
    }
}

//显示字符串
//x,y:起点坐标
//size1:字体大小
//*chr:字符串起始地址
//mode:0,反色显示;1,正常显示
void ShowString(unsigned char page,unsigned char CaddrH,unsigned char CaddrL,unsigned char *chr,u8 size1)
{
    while((*chr>=' ')&&(*chr<='~'))//判断是不是非法字符!
    {
        //OLED_ShowChar(x,y,*chr,size1,mode);
        dispchar(page,CaddrH,CaddrL,size1,*chr);
        if(size1==16)
        {
            CaddrL+=8;
            if(CaddrL>0xf)
            {
                CaddrL-=0xf;
                CaddrH++;
            }
            if(CaddrH>0x18)
            {
                CaddrH = 0x10;
                page++;
            }
        }
        else if(size1 == 24)
        {
            CaddrL+=12;
            if(CaddrL>0xf)
            {
                CaddrL-=0xf;
                CaddrH++;
            }
            if(CaddrH>0x18)
            {
                CaddrH = 0x10;
                page++;
            }
        }
        else
        {
            CaddrL+=6;
            if(CaddrL>0xf)
            {
                CaddrL-=0xf;
                CaddrH++;
            }
            if(CaddrH>0x18)
            {
                CaddrH = 0x10;
                page++;
            }
        }
        chr++;
    }
}


void LcdInitial()
{
    GPIO_InitTypeDef  GPIO_InitStructure;
    RCC_APB2PeriphClockCmd(	RCC_APB2Periph_GPIOC, ENABLE );	//使能GPIOB时钟
    GPIO_InitStructure.GPIO_Pin = GPIO_Pin_1|GPIO_Pin_3|GPIO_Pin_13;
    GPIO_InitStructure.GPIO_Mode = GPIO_Mode_Out_PP;
    GPIO_InitStructure.GPIO_Speed = GPIO_Speed_50MHz;
    GPIO_Init(GPIOC, &GPIO_InitStructure);
    GPIO_SetBits(GPIOC,GPIO_Pin_1|GPIO_Pin_3|GPIO_Pin_13);


    RCC_APB2PeriphClockCmd(	RCC_APB2Periph_GPIOF, ENABLE );	//使能GPIOF时钟
    GPIO_InitStructure.GPIO_Pin = GPIO_Pin_1|GPIO_Pin_2|GPIO_Pin_3;
    GPIO_InitStructure.GPIO_Mode = GPIO_Mode_Out_PP;
    GPIO_InitStructure.GPIO_Speed = GPIO_Speed_50MHz;
    GPIO_Init(GPIOF, &GPIO_InitStructure);
    GPIO_SetBits(GPIOF,GPIO_Pin_1|GPIO_Pin_2|GPIO_Pin_3);



    LcdReset();
//	LCD_light_0;
    DelayXms(20);

    SendCmdByte(SegDirection);
    DelayXms(20);
    SendCmdByte(ComDirection);
    DelayXms(20);

    SendCmdByte(BiasSet);
    SendCmdByte(RratioSet);
    SendCmdWord(EVcomd,EVcomdValue);
    DelayXms(20);
    SendCmdByte(PowerControl1);
    DelayXms(20);
    SendCmdByte(PowerControl2);
    DelayXms(20);
    SendCmdByte(PowerControl3);
    DelayXms(20);


    SendCmdByte(StartLine);
    SendCmdWord(ColumnAddrH,ColumnAddrL);
    SendCmdByte(PageAddr);
    SendCmdByte(DispON);
    LcdFill(0x00,0x00,0x00,0x00);
}













