int WeekYearday(int years, int months, int days)
{
    int WeekDay=-1;
    if(1==months || 2==months)
    {
        months+=12;
        years--;
    }
    WeekDay=(days+1+2*months+3*(months+1)/5+years+years/4-years/100+years/400)%7;

    return WeekDay;
}

