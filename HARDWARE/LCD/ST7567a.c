void DelayXms(uint16_t nms)
{
    uint16_t i,j;
    for(i = 0 ; i < nms ; i++)
        for(j = 0 ; j <1150 ; j++);
}
void DelayXus(uint16_t nus)
{
    uint16_t i;
    for(i = 0; i< nus; i++);
}
void SendByte(unsigned char databyte)
{

    unsigned char i;

    for(i=0; i<8; i++)
    {
//		 SID=(bit)(databyte&0x80);
        if(databyte&0x80)
        {
            ST7567_SDIN_1;//
        }
        else
        {
            ST7567_SDIN_0;//
        }


        ST7567_SCLK_1;
        DelayXus(2);

        ST7567_SCLK_0;
        DelayXus(2);
        databyte=databyte<<1;
    }
}

void SendCmdWord(unsigned char cmdh,unsigned char cmdl)
{
    ST7567_A0_0;
    ST7567_CS_0;
    SendByte(cmdh);
    ST7567_CS_1;

    ST7567_A0_0;
    ST7567_CS_0;
    SendByte(cmdl);
    ST7567_CS_1;

}

void SendCmdByte(unsigned char cmd1)
{
    ST7567_A0_0;
    ST7567_CS_0;
    SendByte(cmd1);
    ST7567_CS_1;

}

void SendDataByte(unsigned char data1)
{

    ST7567_A0_1;
    ST7567_CS_0;
    SendByte(data1);
    ST7567_CS_1;
}

void LcdReset()
{   ST7567_CS_0;
    ST7567_RST_1;
    DelayXms(10);
    ST7567_RST_0;
    DelayXms(100);
    ST7567_RST_1;
    DelayXms(50);
}


